/* WAP to use built-in methods in Math class */

class mathClass{
	public static void main (String[] args) {
		System.out.println(Math.abs(-2.9)); //absolute value
		
		System.out.println(Math.ceil(2.3)); //round off to upper whole number 
		
		System.out.println(Math.floor(2.9)); //rounds off to down the number
		
		System.out.println(Math.max(5.6, 8.2)); // maximum of two numbers
		
		System.out.println(Math.min(2.9, 5.2)); //min of two numbers
		
		System.out.println(Math.pow(5, 3)); //five to the 3rd power
		
		System.out.println(Math.sqrt(9)); //sqaure root of given parameter.
		
	}
}